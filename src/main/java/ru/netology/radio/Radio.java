// Пакет, содержащий классы для работы с радио
package ru.netology.radio;

/**
 * Класс Radio моделирует поведение радиоприемника
 * с управлением станцией и громкостью.
 *
 * Основные возможности:
 * 1. Выбор радиостанции (0-9)
 * 2. Переключение станций вперед/назад
 * 3. Регулировка громкости (0-100)
 *
 * Ограничения:
 * - Допустимые номера станций: 0-9
 * - Допустимые значения громкости: 0-100
 * - При выходе за границы значения не изменяются
 */
public class Radio {
    /**
     * Текущая выбранная радиостанция.
     * Диапазон допустимых значений: от 0 до 9 включительно.
     * При создании объекта инициализируется значением по умолчанию (0).
     */
    private int currentStation;

    /**
     * Текущий уровень громкости.
     * Диапазон допустимых значений: от 0 до 100 включительно.
     * При создании объекта инициализируется значением по умолчанию (0).
     */
    private int currentVolume;

    /**
     * Возвращает текущую выбранную радиостанцию.
     *
     * @return номер текущей станции (0-9)
     */
    public int getCurrentStation() {
        return currentStation;
    }

    /**
     * Устанавливает указанную радиостанцию.
     * Если переданный номер станции находится в допустимом диапазоне (0-9),
     * значение устанавливается. В противном случае станция не изменяется.
     *
     * @param station номер станции для установки (0-9)
     */
    public void setCurrentStation(int station) {
        // Проверка корректности введенного номера станции
        if (station >= 0 && station <= 9) {
            this.currentStation = station;
        }
        // Если station не в диапазоне 0-9, значение currentStation не изменяется
    }

    /**
     * Переключает на следующую радиостанцию.
     * При достижении максимальной станции (9) происходит циклическое
     * переключение на минимальную станцию (0).
     */
    public void next() {
        if (currentStation == 9) {
            // Циклический переход: после 9-й станции следует 0-я
            currentStation = 0;
        } else {
            // Обычное увеличение номера станции на 1
            currentStation++;
        }
    }

    /**
     * Переключает на предыдущую радиостанцию.
     * При достижении минимальной станции (0) происходит циклическое
     * переключение на максимальную станцию (9).
     */
    public void prev() {
        if (currentStation == 0) {
            // Циклический переход: перед 0-й станцией следует 9-я
            currentStation = 9;
        } else {
            // Обычное уменьшение номера станции на 1
            currentStation--;
        }
    }

    /**
     * Возвращает текущий уровень громкости.
     *
     * @return текущая громкость (0-100)
     */
    public int getCurrentVolume() {
        return currentVolume;
    }

    /**
     * Увеличивает громкость на 1 единицу.
     * Если текущая громкость уже максимальна (100),
     * значение не изменяется.
     */
    public void increaseVolume() {
        if (currentVolume < 100) {
            // Увеличение громкости, если не достигнут максимум
            currentVolume++;
        }
        // При currentVolume == 100 громкость не изменяется
    }

    /**
     * Уменьшает громкость на 1 единицу.
     * Если текущая громкость уже минимальна (0),
     * значение не изменяется.
     */
    public void decreaseVolume() {
        if (currentVolume > 0) {
            // Уменьшение громкости, если не достигнут минимум
            currentVolume--;
        }
        // При currentVolume == 0 громкость не изменяется
    }
}